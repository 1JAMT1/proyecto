    <h1 class="titulo-contacto text-center">REEMBOLSOS</h1>
    <h3>1. Libros defectuosos o dañados:</h3>
    <P>Si un cliente recibe un libro en mal estado o defectuoso, el espacio de literatura puede ofrecer reembolsos para garantizar la satisfacción del cliente.</P>
    <h3>2. Cancelaciones de pedidos:</h3>
    <p>Si un cliente desea cancelar un pedido antes de que sea enviado, el espacio de literatura puede ofrecer reembolsos completos o parciales según su política de cancelación.</p>
    <h3>3. Devoluciones por insatisfacción:</h3>
    <p>Si un cliente no está satisfecho con un libro que ha comprado, es posible que desee devolverlo y recibir un reembolso. El espacio de literatura podría ofrecer reembolsos en estos casos, según su política de devolución.</p>
    <h3>4. Errores en el procesamiento de pedidos:</h3>
    <p>Si se comete un error durante el procesamiento de un pedido, como enviar el libro incorrecto o enviar la cantidad incorrecta de libros, el espacio de literatura podría ofrecer reembolsos para rectificar el error.</p>
    <h3>5. Cambios en las condiciones de venta:</h3>
    <p>Si el espacio de literatura cambia las condiciones de venta después de que un cliente haya realizado una compra, como cambios en el precio o en las condiciones de envío, podría ofrecer reembolsos a los clientes que ya han realizado compras bajo las condiciones anteriores.</p>
