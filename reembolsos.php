    <h5>Si un cliente desea cancelar un pedido antes de que sea enviado, el espacio de literatura puede ofrecer reembolsos completos o parciales según su política de cancelación.</h5>
    <h2>3. Devoluciones por insatisfacción:</h2>
    <h5>Si un cliente no está satisfecho con un libro que ha comprado, es posible que desee devolverlo y recibir un reembolso. El espacio de literatura podría ofrecer reembolsos en estos casos, según su política de devolución.</h5>
    <h2>4. Errores en el procesamiento de pedidos:</h2>
    <h5>Si se comete un error durante el procesamiento de un pedido, como enviar el libro incorrecto o enviar la cantidad incorrecta de libros, el espacio de literatura podría ofrecer reembolsos para rectificar el error.</h5>
    <h2>5. Cambios en las condiciones de venta:</h2>
    <h5>Si el espacio de literatura cambia las condiciones de venta después de que un cliente haya realizado una compra, como cambios en el precio o en las condiciones de envío, podría ofrecer reembolsos a los clientes que ya han realizado compras bajo las condiciones anteriores.</h5>
